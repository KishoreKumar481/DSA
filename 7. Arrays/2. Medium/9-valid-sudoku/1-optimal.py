from collections import defaultdict


def isValidSudoku(board):
    rows = defaultdict(set)
    columns = defaultdict(set)
    squares = defaultdict(set)

    for r in range(9):
        for c in range(9):
            val = board[r][c]

            if val == ".":
                continue

            square_index = (r // 3, c // 3)

            if val in rows[r] or val in columns[c] or val in squares[square_index]:
                return False

            rows[r].add(val)
            columns[c].add(val)
            squares[square_index].add(val)

    return True


